function sortByDate(arr) {
    arr.sort(function (a, b) {
        return Number(new Date(a.ViewedDate)) - Number(new Date(b.ViewedDate));
    });
    return arr;
}

function sortByPropertyDetailId(arr) {
    arr.sort(function (a, b) {
        return a.PropertyDetailId - b.PropertyDetailId;
    });
    return arr;
}

